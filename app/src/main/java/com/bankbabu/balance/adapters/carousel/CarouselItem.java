package com.bankbabu.balance.adapters.carousel;

public class CarouselItem {

    private String title;
    private String body;

    public CarouselItem(String title, String body) {
        this.title = title;
        this.body = body;
    }

    public String getTitle() {
        return title;
    }

    public String getBody() {
        return body;
    }
}
